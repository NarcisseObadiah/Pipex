#include "libft.h"

void	ft_putnbr_fd(int n, int fd)
{
	unsigned int	nbr;
	int				sign;

	if (n < 0)
	{
		ft_putchar_fd('-', fd);
		sign = -1;
		nbr = (unsigned int)(n * sign);
	}
	else
		nbr = (unsigned int)n;
	if (nbr >= 10)
		ft_putnbr_fd(nbr / 10, fd);
	ft_putchar_fd((char)(nbr % 10 + '0'), fd);
}
